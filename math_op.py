first_num = int(input("Enter first number: "))
second_num = int(input("Enter second number: "))
plus_num = first_num + second_num
minus_num = first_num - second_num
multiply_num = first_num * second_num
divide_num = first_num / second_num
divide_floor_num = first_num // second_num
mod_num = first_num % second_num
power_num = first_num**second_num
print(f"{first_num} + {second_num} = {plus_num}")
print(f"{first_num} - {second_num} = {minus_num}")
print(f"{first_num} * {second_num} = {multiply_num}")
print(f"{first_num} / {second_num} = {divide_num}")
print(f"{first_num} // {second_num} = {divide_floor_num}")
print(f"{first_num} % {second_num} = {mod_num}")
print(f"{first_num} ** {second_num} = {power_num}")
